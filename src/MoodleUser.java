public class MoodleUser {
    double loen;
    String navn;
    int lokale;

    public double getLoen() {
        return loen;
    }

    public void setLoen(double loen) {
        this.loen = loen;
    }

    public String getNavn() {
        return navn;
    }

    public void setNavn(String navn) {
        this.navn = navn;
    }

    public int getLokale() {
        return lokale;
    }

    public void setLokale(int lokale) {
        this.lokale = lokale;
    }
}
